/**
 * Snake
 */
public class Snake {
    private int startPoint;
    private int endPoint;

    public Snake(int startPoint, int endPoint) {
        this.startPoint = startPoint;
        this.endPoint = endPoint;
    }

    public int getStartPoint() {
        return startPoint;
    }

    public int getEndPoint() {
        return endPoint;
    }
    
}